create policy "profiles_select_own" on public.profiles
for select to authenticated
using ((select auth.uid()) = id);

create policy "profiles_insert_own" on public.profiles
for insert to authenticated
with check ((select auth.uid()) = id);

create policy "profiles_update_own" on public.profiles
for update to authenticated
using ((select auth.uid()) = id)
with check ((select auth.uid()) = id);

create policy "routines_manage_own" on public.routines
for all to authenticated
using ((select auth.uid()) = owner_id)
with check ((select auth.uid()) = owner_id);

create policy "routine_days_manage_own" on public.routine_days
for all to authenticated
using (
  exists (
    select 1
    from public.routines
    where public.routines.id = routine_id
      and public.routines.owner_id = (select auth.uid())
  )
)
with check (
  exists (
    select 1
    from public.routines
    where public.routines.id = routine_id
      and public.routines.owner_id = (select auth.uid())
  )
);

create policy "routine_day_exercises_manage_own" on public.routine_day_exercises
for all to authenticated
using (
  exists (
    select 1
    from public.routine_days
    join public.routines on public.routines.id = public.routine_days.routine_id
    where public.routine_days.id = routine_day_id
      and public.routines.owner_id = (select auth.uid())
  )
)
with check (
  exists (
    select 1
    from public.routine_days
    join public.routines on public.routines.id = public.routine_days.routine_id
    where public.routine_days.id = routine_day_id
      and public.routines.owner_id = (select auth.uid())
  )
);

